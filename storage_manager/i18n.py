def tr(language: str, key: str, **values: object) -> str:
    language = language if language in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE
    translations = TEXTS.get(key)
    if translations is None:
        return key
    template = translations.get(language) or translations["en"]
    return template.format(**values)
